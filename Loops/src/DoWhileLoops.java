public class DoWhileLoops {    
    public static void main(String[] args) throws Exception {
        // Declaring and initialization expression
        int i = 1;
    
        // Do-while loop
        do {
            // Body od do-while loopPrint statement
            System.out.println("Hello World");

            // Update expression
            i++;
        }// Test expression
        while (i < 6);
    }
}
